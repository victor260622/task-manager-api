import { IsEmail, IsString, MinLength, IsOptional, IsUrl } from 'class-validator';

export class RegisterDto {
  @IsEmail()
  email!: string;

  @IsString()
  @MinLength(8)
  password!: string;

  @IsString()
  firstName!: string;

  @IsString()
  lastName!: string;

  @IsOptional()
  @IsUrl()
  avatarUrl?: string;
}